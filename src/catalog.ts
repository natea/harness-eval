/**
 * Target catalog: a manifest-derived, drift-checked `docs/TARGETS.md` plus the
 * listing helpers the CLI and Studio use to show operators what a target builds
 * BEFORE selecting it (eval-targets spec: "Manifest-derived catalog document").
 *
 * The manifests are the single source of truth — this module only reads them, so
 * the doc cannot drift: `checkCatalog()` regenerates in memory and compares.
 */
import { existsSync, readdirSync, readFileSync } from "node:fs";
import { join } from "node:path";
import { type LoadedTarget, loadTarget } from "./targets";

export const CATALOG_PATH = "docs/TARGETS.md";

export interface CatalogEntry {
	name: string;
	version: string;
	summary: string;
	description: string;
	domain: string;
	shape: string;
	expectedUI: string;
	upstream?: string;
}

/** Every target directory under `targetsDir` (those with a target.yaml), sorted. */
export function targetNames(targetsDir = "targets"): string[] {
	if (!existsSync(targetsDir)) return [];
	return readdirSync(targetsDir)
		.filter((d) => existsSync(join(targetsDir, d, "target.yaml")))
		.sort();
}

function toEntry(t: LoadedTarget): CatalogEntry {
	const m = t.manifest;
	return {
		name: m.name,
		version: m.version,
		summary: m.summary,
		description: m.description.trim(),
		domain: m.tags.domain,
		shape: m.tags.shape,
		expectedUI: m.tags.expectedUI,
		upstream: m.source?.upstream,
	};
}

/** Load every target's catalog entry (validates each manifest as a side effect). */
export function loadCatalog(targetsDir = "targets"): CatalogEntry[] {
	return targetNames(targetsDir).map((n) => toEntry(loadTarget(n, targetsDir)));
}

/**
 * Like `loadCatalog` but skips targets that fail to load, for UI surfaces (the
 * Studio picker) that must not 500 because one target is mid-edit. The CLI's
 * `validate`/`catalog` use the strict `loadCatalog` instead so failures surface.
 */
export function loadCatalogSafe(targetsDir = "targets"): CatalogEntry[] {
	const out: CatalogEntry[] = [];
	for (const n of targetNames(targetsDir)) {
		try {
			out.push(toEntry(loadTarget(n, targetsDir)));
		} catch {
			// skip unloadable target
		}
	}
	return out;
}

/**
 * Render `docs/TARGETS.md` from the entries. Generated banner up top so no one
 * hand-edits it; LF newlines + a single trailing newline so the drift check is
 * stable across platforms.
 */
export function renderCatalog(entries: CatalogEntry[]): string {
	const lines: string[] = [];
	lines.push("<!-- GENERATED by `bun run src/cli.ts catalog` — do not edit by hand. -->");
	lines.push("<!-- Source of truth: each targets/<name>/target.yaml. Run the command to refresh. -->");
	lines.push("");
	lines.push("# Eval target catalog");
	lines.push("");
	lines.push(
		"What each shipped eval target builds, and how much rendered UI a *conformant* build actually has (`expectedUI`). HTTP-light targets grade the API contract, not the DOM — so `served-page` means a served HTML shell whose content is not graded.",
	);
	lines.push("");
	lines.push("| Target | Shape | Expected UI | Summary |");
	lines.push("| --- | --- | --- | --- |");
	for (const e of entries) {
		lines.push(`| \`${e.name}\` | ${e.shape} | ${e.expectedUI} | ${e.summary} |`);
	}
	lines.push("");
	for (const e of entries) {
		lines.push(`## \`${e.name}\` (v${e.version})`);
		lines.push("");
		lines.push(`- **Domain:** ${e.domain}`);
		lines.push(`- **Shape:** ${e.shape}`);
		lines.push(`- **Expected UI:** ${e.expectedUI}`);
		if (e.upstream) lines.push(`- **Adapted from:** ${e.upstream}`);
		lines.push("");
		lines.push(e.description);
		lines.push("");
	}
	return `${lines.join("\n").trimEnd()}\n`;
}

export function generateCatalog(targetsDir = "targets"): string {
	return renderCatalog(loadCatalog(targetsDir));
}

/** Compare the committed catalog doc to a fresh render. */
export function checkCatalog(
	targetsDir = "targets",
	catalogPath = CATALOG_PATH,
): { stale: boolean; expected: string; actual: string | null } {
	const expected = generateCatalog(targetsDir);
	const actual = existsSync(catalogPath)
		? readFileSync(catalogPath, "utf8")
		: null;
	return { stale: actual !== expected, expected, actual };
}
